public class PrintHello{
   public static void main(String[] args){
      //Date: August 26th, 2021
      //We have 3 sequential statements
      int i = 10; //Sequential
      while(i > 0){ //Iterative
         System.out.println("Hello");
         i = i -1; //decreases i by 1
      }
      System.out.println(i);
   }
}